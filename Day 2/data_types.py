# Pulling out character from the string is called subscripting
print("subscripting"[0])
print("subscripting"[4])

print("subscripting"[-1])
print("subscripting"[-6])

print("132" + "345") # Concatenation

# Integer = Whole Numbers
print(123 + 345)

# Large Integers --> Commas are replaced by (_)
print(10_000_000)

# Float = Floating Point Numbers
print(123.56)

# Boolean
print(True)
print(False)
